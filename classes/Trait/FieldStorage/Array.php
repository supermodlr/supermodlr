<?php

trait Trait_FieldStorage_Array {

    public function validate_storage($value) {
        return (is_array($value) && !$this->is_assoc($value));

    }

    public function validate_datatype_values($value) {
        foreach ($value as $i => $v)
        {
            $valid = $this->validate_datatype($v);
            if ($valid !== TRUE)
            {
                return FALSE;
            }
        }
    }

    public function store_value($value,$method = 'set', $args = NULL)
    {
        $values = array();
        $method = $method.'_value';
        foreach ($value as $i => $v)
        {
            $values[] = $this->$method($v, $args);
        }
        return $values;
    }
}